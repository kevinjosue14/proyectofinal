    private void mostrarArchivo(){
          String id, Nombre, Autor, Editorial, anio;
        
        String au = "C:\\Ventas\\admin\\usuario.txt";
        
        File arcLectura = new File(au);
        try {
           Scanner lecturaArchivo = new Scanner(arcLectura);
           
           while(lecturaArchivo.hasNextLine()){
               id = lecturaArchivo.nextLine();
               Nombre = lecturaArchivo.nextLine();
               Autor = lecturaArchivo.nextLine();
               Editorial = lecturaArchivo.nextLine();
               anio = lecturaArchivo.nextLine();
               
               dtm.addRow(new Object[]{id, Nombre, Autor, Editorial, anio});
           }
        }catch (FileNotFoundException e) {
             JOptionPane.showMessageDialog(null, "Erorr" + e);
        }
    
    }
    
